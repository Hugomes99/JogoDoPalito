package com.example.jogodopalito;

public class Palitos {
    private int contPalito, jogadaPalito, apostaPalito;



    public int getContPalito() {
        return contPalito;
    }

    public void setContPalito(int contPalito) {
        this.contPalito = contPalito;
    }

    public int getJogadaPalito() {
        return jogadaPalito;
    }

    public void setJogadaPalito(int jogadaPalito) {
        this.jogadaPalito = jogadaPalito;
    }

    public int getApostaPalito() {
        return apostaPalito;
    }

    public void setApostaPalito(int apostaPalito) {
        this.apostaPalito = apostaPalito;
    }
}
